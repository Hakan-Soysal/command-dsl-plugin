#!/usr/bin/env node
// spec-context eval — byte-stability + snapshot + coverage (her manifest yükümlülüğü çıktıda mı, fazlası yok mu).
import { execFileSync } from 'node:child_process';
import { readFileSync, mkdtempSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';
// fileURLToPath: boşluklu proje yolunda `.pathname` %20-encode edip var-olmayan dizin üretir (spawn ENOENT).
// Plugin genelindeki tüm runner'lar bu idiom'u kullanır — byte-stable + boşluk-güvenli.
const here = fileURLToPath(new URL('.', import.meta.url));
const skillDir = join(here, '..');
const fixture = join(skillDir, 'spec', 'manifest.json'); // absolute — yalnız coverage readFileSync için
// KRİTİK: CLI'ı cwd=skillDir + RELATIVE pozisyonel ('spec/manifest.json') ile koş — böylece provenance
// `source:` satırı Step 5.2'de üretilen snapshot ile AYNI olur. Absolute path geçmek header'a absolute
// `source:` gömer → byte-stable kalır ama snapshot != expected (source satırı farklı) → 6.1 fail, 6.2 pass
// (sessiz uyuşmazlık). Snapshot (Step 5.2) ve run.mjs AYNI pozisyonel string'i kullanmalı.
const run = () => { const d = mkdtempSync(join(tmpdir(), 'sc-')); execFileSync('node', ['spec-context.mjs', 'spec/manifest.json', '--out', d, '--quiet'], { cwd: skillDir }); return readFileSync(join(d, 'CLAUDE.md'), 'utf8'); };
let p = 0, f = 0; const chk = (n, ok) => { console.log(`  ${ok ? 'PASS' : 'FAIL'} ${n}`); ok ? p++ : f++; };
const a = run(), b = run();
chk('byte-stable (iki koşum özdeş)', a === b);
const expected = readFileSync(join(here, 'expected-CLAUDE.md'), 'utf8');
chk('snapshot eşleşir', a === expected);
const man = JSON.parse(readFileSync(fixture, 'utf8'));
// coverage: her yükümlülük metni çıktıda
const musts = [];
for (const op of man.operations) { for (const r of op.roles) musts.push(r); for (const s of op.scopes) musts.push(s); if (op.ownership) musts.push(op.ownership); for (const v of op.validation) musts.push(v.text); for (const ru of op.rule) musts.push(ru.text); for (const t of op.throws) musts.push(t); }
for (const e of man.entities) for (const inv of e.invariants) musts.push(inv.text);
chk('coverage: tüm yükümlülük metinleri çıktıda', musts.every((s) => a.includes(s)));
chk('no-hallucination: Ping (yükümlülüksüz) çıktıda YOK', !a.includes('### Ping'));
chk('no-hallucination: AuditLog (invariantsız) çıktıda YOK', !a.includes('### AuditLog'));
chk('provenance sha256 var', /sha256:[0-9a-f]{64}/.test(a));
chk('no timestamp (byte-stability)', !/\d{4}-\d\d-\d\dT/.test(a));
// ── collision guard tests ──
import { writeFileSync, existsSync as exists } from 'node:fs';
function runOut(outDir, extra = []) { try { execFileSync('node', ['spec-context.mjs', 'spec/manifest.json', '--out', outDir, '--quiet', ...extra], { cwd: skillDir }); return 0; } catch (e) { return e.status ?? 1; } }
const cd = mkdtempSync(join(tmpdir(), 'sc-col-'));
const humanFile = join(cd, 'CLAUDE.md'); const humanText = '# Benim elle yazdığım notlar\n';
writeFileSync(humanFile, humanText);
const code1 = runOut(cd);
chk('collision: insan CLAUDE.md korundu (exit 3)', code1 === 3 && readFileSync(humanFile, 'utf8') === humanText);
const code1f = runOut(cd, ['--force']);
chk('collision: --force insan dosyasını ezer (exit 0)', code1f === 0 && readFileSync(humanFile, 'utf8').includes('GENERATED by spec-context'));
const cd2 = mkdtempSync(join(tmpdir(), 'sc-gen-'));
runOut(cd2); const gen1 = readFileSync(join(cd2, 'CLAUDE.md'), 'utf8');
const code2 = runOut(cd2); // ikinci koşum: generated dosya var → ez
chk('collision: generated CLAUDE.md ezilir/refresh (exit 0)', code2 === 0 && readFileSync(join(cd2, 'CLAUDE.md'), 'utf8') === gen1);
console.log(`EVAL: ${p} pass, ${f} fail`); if (f) process.exitCode = 1;
